            axios.get('https://restcountries.com/v3.1/region/americas')
                .then(response => {
                    let html = '';
                    response.data.forEach(paisDatos => {
                        const pais = paisDatos.name.common;
                        html += `<option value="${pais}">${pais}</option>`;
                    });

                    document.getElementById('pais').innerHTML = html;
                })
                .catch(error => {
                    console.error(error);
                });
        });
    </script>
